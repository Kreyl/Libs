int16_t cosineFromZeroToHalfPi(uint16_t numerator, uint16_t denominator)
{
    size_t point = static_cast<size_t>((static_cast<uint32_t>(numerator) * POINTS / denominator) % (POINTS * 4));

    if (point <= POINTS) {
        return cosine_table[point];
    } else if (point <= 2 * POINTS) {
        return -cosine_table[2 * POINTS - point];
    } else if (point <= 3 * POINTS) {
        return -cosine_table[point - 2 * POINTS];
    } else if (point < 4 * POINTS) {
        return cosine_table[4 * POINTS - point];
    }

    // Control never reaches here
    return 0;
}
